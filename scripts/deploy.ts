// scripts/deploy.ts - Hardhat deployment script (TypeScript/ethers.js equivalent of Deploy.s.sol).
// Run with: npm run hardhat:deploy:local | npm run hardhat:deploy:sepolia
// Writes deployed addresses to artifacts/deployed-addresses.json for frontend consumption.

import { writeFileSync, mkdirSync } from "node:fs";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { network } from "hardhat";
import type { TrustLedger as TrustLedgerContract } from "../artifacts/typechain-types";
import { syncFrontendEnv } from "./sync-frontend-env";

const { ethers } = await network.create();
const __dirname = fileURLToPath(new URL(".", import.meta.url));

// Hardhat's built-in local node chain ID. Only on a local chain do we mirror the
// deployed addresses into src/.env.local; live deploys feed Vercel via the
// GitHub Actions workflow instead.
const HARDHAT_LOCAL_CHAIN_ID = 31337n;

async function main(): Promise<void> {
	// ethers.getSigners() returns all accounts configured in hardhat.config.ts for the
	// active network. For sepolia, this is the DEPLOYER_PRIVATE_KEY wallet.
	// Destructuring assignment: `[deployer]` picks only the first signer.
	const [deployer] = await ethers.getSigners();
	console.log("Deploying with:", deployer.address);

	// Read the deployer's current nonce (transaction count).
	// Like vm.getNonce() in Foundry - the nonce tells us where the next contracts will land.
	const nonce = await deployer.getNonce();

	// ── Precompute contract addresses ──────────────────────────────────────────
	// On EVM, CREATE opcode addresses are deterministic:
	//   address = keccak256(RLP(deployer, nonce))[12:]
	// ethers.getCreateAddress() computes this without deploying anything.
	// We need this because TrustLedger and JurorRegistry must know Arbitration's
	// address BEFORE it's deployed (all three have immutable cross-references).
	//
	// Deploy order → nonce mapping:
	//   nonce   → JurorRegistry (deployed first)
	//   nonce+1 → TrustLedger  (deployed second)
	//   nonce+2 → Arbitration  (deployed third - address is what we precompute)
	const arbitrationAddr = ethers.getCreateAddress({ from: deployer.address, nonce: nonce + 2 });
	// const trustLedgerAddr = ethers.getCreateAddress({ from: deployer.address, nonce: nonce + 1 });

	// ── Deploy JurorRegistry ───────────────────────────────────────────────────
	// ethers.getContractFactory("JurorRegistry") reads the compiled ABI + bytecode
	// from artifacts/ (generated by `npm run compile`) and creates a factory object.
	const JurorRegistry = await ethers.getContractFactory("JurorRegistry");

	// factory.deploy(...args) sends a CREATE transaction to the network.
	// We pass arbitrationAddr even though Arbitration doesn't exist yet -
	// the constructor just stores it as a plain address.
	const jurorRegistry = await JurorRegistry.deploy(arbitrationAddr);

	// waitForDeployment() waits for the transaction to be mined (included in a block).
	// Without this, the next deploy might use a stale nonce.
	await jurorRegistry.waitForDeployment();
	console.log("JurorRegistry:", await jurorRegistry.getAddress());

	// ── Deploy TrustLedger ────────────────────────────────────────────────────
	const TrustLedger = await ethers.getContractFactory("TrustLedger");
	const trustLedger = (await TrustLedger.deploy(
		arbitrationAddr,
	)) as unknown as TrustLedgerContract;
	await trustLedger.waitForDeployment();
	console.log("TrustLedger:  ", await trustLedger.getAddress());

	// ── Deploy Arbitration ────────────────────────────────────────────────────
	// Now that JurorRegistry and TrustLedger exist, we can deploy Arbitration with
	// their real addresses. Arbitration also lands at nonce+2, matching our prediction.
	const Arbitration = await ethers.getContractFactory("Arbitration");
	const arbitration = await Arbitration.deploy(
		await trustLedger.getAddress(),
		await jurorRegistry.getAddress(),
	);
	await arbitration.waitForDeployment();
	console.log("Arbitration:  ", await arbitration.getAddress());

	// ── Verify the address matched our prediction ─────────────────────────────
	// If nonce tracking was off (e.g. a failed tx incremented the nonce), the
	// Arbitration contract would land at the wrong address, breaking JurorRegistry
	// and TrustLedger's immutable references. This check catches that immediately.
	const actualAddr = await arbitration.getAddress();
	if (actualAddr.toLowerCase() !== arbitrationAddr.toLowerCase()) {
		throw new Error(`Address mismatch: expected ${arbitrationAddr}, got ${actualAddr}`);
	}

	console.log("\nDeployment verified successfully!");
	console.log("All contract addresses match precomputed values.");

	// ── Deploy ReputationRegistry ──────────────────────────────────────────────
	// Deployed after Arbitration so the nonce+2 precomputation above is unaffected.
	const ReputationRegistry = await ethers.getContractFactory("ReputationRegistry");
	const reputationRegistry = await ReputationRegistry.deploy(await trustLedger.getAddress());
	await reputationRegistry.waitForDeployment();
	const reputationRegistryReceipt = await reputationRegistry.deploymentTransaction()?.wait();
	console.log("ReputationRegistry:", await reputationRegistry.getAddress());

	// Wire ReputationRegistry into TrustLedger so submitRating() is live.
	await (await trustLedger.initReputationRegistry(await reputationRegistry.getAddress())).wait();
	console.log("Wired: TrustLedger -> ReputationRegistry");

	// ── Write deployed addresses for frontend consumption ─────────────────────
	// artifacts/ is gitignored; this file is regenerated on every deploy.
	// Import it in the frontend as: import addrs from "../../artifacts/deployed-addresses.json"
	const addresses = {
		JurorRegistry: await jurorRegistry.getAddress(),
		TrustLedger: await trustLedger.getAddress(),
		Arbitration: await arbitration.getAddress(),
		ReputationRegistry: await reputationRegistry.getAddress(),
		TrustLedgerDeployBlock: reputationRegistryReceipt?.blockNumber ?? 0,
		network: (await ethers.provider.getNetwork()).name,
		deployedAt: new Date().toISOString(),
	};
	const outDir = resolve(__dirname, "../artifacts");
	mkdirSync(outDir, { recursive: true });
	writeFileSync(resolve(outDir, "deployed-addresses.json"), JSON.stringify(addresses, null, 2));
	console.log("\nAddresses written to artifacts/deployed-addresses.json");

	// On the local node, mirror the addresses into src/.env.local so the Next.js
	// dev server resolves the contracts (incl. ReputationRegistry) without a
	// manual copy step. Skipped on live networks (Vercel is updated by CI).
	const chainId = (await ethers.provider.getNetwork()).chainId;
	if (chainId === HARDHAT_LOCAL_CHAIN_ID) {
		syncFrontendEnv();
	}
}

// Standard Node.js async error handling pattern.
// `main()` returns a Promise; `.catch()` handles any thrown error,
// and `process.exit(1)` signals failure to the shell (CI pipelines check this).
main().catch((err: unknown): void => {
	console.error(err);
	process.exit(1);
});
