import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outputPath = path.join(repoRoot, "k8s", "secret.yaml");

const secretKeys = [
	"ADMIN_API_TOKEN",
	"CRON_SECRET",
	"HEALTH_CHECK_TOKEN",
	"MAGIC_LINK_SECRET",
	"NOTIFICATIONS_SECRET",
	"RESEND_API_KEY",
	"SEPOLIA_RPC_URL",
];

function yamlString(value) {
	return JSON.stringify(value);
}

const entries = secretKeys
	.map((key) => [key, process.env[key] ?? ""])
	.filter(([, value]) => value !== "");

if (entries.length === 0) {
	console.error(`No Kubernetes secret values found. Set one of: ${secretKeys.join(", ")}`);
	process.exit(1);
}

const body = [
	"# Generated by `npm run k8s:secret:generate`.",
	"# This file is ignored by git. Do not commit real cluster secrets.",
	"apiVersion: v1",
	"kind: Secret",
	"metadata:",
	"    name: trustledger-frontend-secrets",
	"    namespace: trustledger",
	"    labels:",
	"        app.kubernetes.io/name: trustledger-frontend",
	"        app.kubernetes.io/part-of: trustledger",
	"type: Opaque",
	"stringData:",
	...entries.map(([key, value]) => `    ${key}: ${yamlString(value)}`),
	"",
].join("\n");

mkdirSync(path.dirname(outputPath), { recursive: true });
writeFileSync(outputPath, body);
console.log(`wrote ${path.relative(repoRoot, outputPath)} with ${entries.length} key(s)`);
